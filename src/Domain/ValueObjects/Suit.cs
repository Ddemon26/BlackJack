namespace GroupProject.Domain.ValueObjects;

/// <summary>
/// Represents the four suits in a standard deck of playing cards.
/// </summary>
public enum Suit
{
    /// <summary>
    /// The spades suit (♠).
    /// </summary>
    Spades,

    /// <summary>
    /// The hearts suit (♥).
    /// </summary>
    Hearts,

    /// <summary>
    /// The diamonds suit (♦).
    /// </summary>
    Diamonds,

    /// <summary>
    /// The clubs suit (♣).
    /// </summary>
    Clubs
}